    private void searchFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFieldKeyPressed
          if(evt.getKeyCode()== evt.VK_ENTER){   
         try {
            String search = "SELECT words, wordtype, definition FROM dictionary where words = ?";
            pst = conn.prepareStatement(search);
            pst.setString(1, searchField.getText());
             rs   = pst.executeQuery();
           //  rs   = stmt.executeQuery(abdicationDfn);
              resWordLbl.setText(rs.getString(1));
              resType.setText(rs.getString(2));
              resDfn.setText(rs.getString(3));
              
        } 
            catch(Exception e){
            JOptionPane.showMessageDialog(null, "Sorry, no definition found. Please try another word!", "Lumen", JOptionPane.WARNING_MESSAGE);        // TODO add your handling code here:
    }
        }// TODO add your handling code here:
    }//GEN-LAST:event_searchFieldKeyPressed

